  function normalizeKkVoiceText(text) {
    return String(text || "")
      .toLocaleLowerCase("kk-KZ")
      .replace(/[()\[\]{}]/g, " ")
      .replace(/[\u2014\u2013-]/g, " ")
      .replace(/[.,!?;:\"'\u00ab\u00bb]/g, " ")
      .replace(/\s+\d+$/g, "")
      .replace(/\s+/g, " ")
      .trim();
  }

